def exponents (base,power) :
    return base**power
print(exponents(2,3))

def circle_a(r,pi=3.14) : # circle's area
    return pi*r*r
print(circle_a(7))

def triangle_a(base,high) : # triangle's area
    return 0.5*base*high
print(triangle_a(15,10))

def squar_a(base1,base2,high) : # trapezium's area (สี่เหลี่ยมคางหมู)
    return 0.5*(base1+base2)*high
print(squar_a(10,15,10))